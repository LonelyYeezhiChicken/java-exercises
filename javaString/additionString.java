package javaString;

public class additionString {
    public static void main(String[] args) {

        // 宣告字串
        String one = "1";

        // 宣告第二字串
        String two = "2";

        // 相加字串
        String ans = "ans=" + one + two;

        // 印出結果
        System.out.println(ans);
    }
}
